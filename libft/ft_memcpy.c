#include <string.h>

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	unsigned long	i;
	char			*tmp;
	char			*tmp2;

	i = 0;
	tmp = (char *)dst;
	tmp2 = (char *)src;
	while (i < n)
	{
		tmp[i] = tmp2[i];
		i++;
	}
	return (dst);
}

#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	size_t			i;
	unsigned char	*tmp;
	unsigned char	*tmp2;

	tmp = (unsigned char*)s1;
	tmp2 = (unsigned char*)s2;
	i = 0;
	while (tmp[i] && tmp2[i] && tmp[i] == tmp2[i])
		i++;
	return (tmp[i] - tmp2[i]);
}

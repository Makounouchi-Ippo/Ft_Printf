#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t				i;
	unsigned char		a;
	unsigned char		*b;

	i = 0;
	b = (unsigned char *)s;
	a = (unsigned char)c;
	while (i < n)
	{
		if (b[i] == a)
			return (&b[i]);
		i++;
	}
	return (NULL);
}
